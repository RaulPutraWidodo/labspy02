# Input dua bilangan
a = float(input("Masukkan bilangan pertama: "))
b = float(input("Masukkan bilangan kedua: "))

# Mencari bilangan terbesar
if a > b:
    bilangan_terbesar = a
else:
    bilangan_terbesar = b

# Menampilkan bilangan terbesar
print("Bilangan terbesar adalah:", bilangan_terbesar)
